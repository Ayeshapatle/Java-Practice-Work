package SecRefTask;

public class University {
	private String uname;
	private String uid;
	private long umobno;
	private College clg;

	public String getUname() {
		return uname;
	}

	public void setUname(String uname) {
		this.uname = uname;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public long getUmobno() {
		return umobno;
	}

	public void setUmobno(long umobno) {
		this.umobno = umobno;
	}

	public College getClg() {
		return clg;
	}

	public void setClg(College clg) {
		this.clg = clg;
	}

}
